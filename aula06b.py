n = input('Digite algo: ')
print('É número:', n.isnumeric())           # é número
print('É letra:' ,n.isalpha())              # É letra
print('É alfanumerico:',n.isalnum())        # É letra e número
print('Está em maiusculas: ', n.isupper())
print('Esta em minuculas: ',  n.islower())

